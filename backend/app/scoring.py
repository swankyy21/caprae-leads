def _text(value: Any, fallback: str = "Unknown") -> str:
    if value is None or value == "":
        return fallback
    return str(value)


def lead_cache_key(lead: dict[str, Any]) -> str:
    identity = (
        lead.get("website")
        or lead.get("domain")
        or lead.get("email")
        or lead.get("company")
        or lead.get("name")
        or json.dumps(lead, sort_keys=True)
    )
    digest = hashlib.sha256(str(identity).lower().encode("utf-8")).hexdigest()
    return f"lead-score:{digest}"


def _fallback_score(lead: dict[str, Any]) -> dict[str, Any]:
    revenue = normalize_number(lead.get("revenue") or lead.get("annual_revenue"))
    employees = normalize_number(lead.get("employees") or lead.get("employee_count"))
    description = " ".join(
        _text(lead.get(key), "")
        for key in ("industry", "sector", "title", "description", "notes")
    ).lower()

    revenue_fit = 85 if 1_000_000 <= revenue <= 20_000_000 else 55 if revenue else 35
    size_fit = 90 if 5 <= employees <= 150 else 62 if employees <= 500 else 42
    industry_fit = 78
    if re.search(r"software|healthcare|services|logistics|manufacturing|b2b", description):
        industry_fit = 86
    if re.search(r"creative|agency|restaurant|retail", description):
        industry_fit = 54

    owner_reachability = 35
    if lead.get("email_valid"):
        owner_reachability += 25
    elif lead.get("email_syntax_valid"):
        owner_reachability += 10
    if lead.get("phone"):
        owner_reachability += 15
    if lead.get("owner") or lead.get("founder") or re.search(r"founder|owner", description):
        owner_reachability += 20
    owner_reachability = min(owner_reachability, 100)

    growth_signals = 45
    if re.search(r"growing|expanded|hiring|new|launch|automation|ai|scale", description):
        growth_signals = 78

    overall = round(
        revenue_fit * 0.25
        + industry_fit * 0.2
        + size_fit * 0.2
        + owner_reachability * 0.2
        + growth_signals * 0.15
    )
    tier = "A" if overall >= 80 else "B" if overall >= 65 else "C" if overall >= 45 else "D"

    return {
        "overall_score": overall,
        "dimensions": {
            "revenue_fit": revenue_fit,
            "industry_fit": industry_fit,
            "size_fit": size_fit,
            "owner_reachability": owner_reachability,
            "growth_signals": growth_signals,
        },
        "tier": tier,
        "acquisition_thesis": "This score was generated by the local development scorer because Claude fallback mode is enabled. Use it only for testing persistence and caching, not production lead decisions.",
        "key_risks": ["Development fallback score", "No Claude reasoning was used"],
        "key_opportunities": ["Validate backend persistence", "Validate Redis cache behavior"],
        "recommended_action": "Pursue" if tier in ("A", "B") else "Monitor",
        "estimated_ev_range": "Unknown",
        "fit_tags": ["dev-fallback", f"tier-{tier.lower()}"],
    }


