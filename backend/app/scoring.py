def _score_with_claude(lead: dict[str, Any]) -> dict[str, Any]:
    if not settings.anthropic_api_key:
        if settings.allow_fallback_scoring:
            return _fallback_score(lead)
        raise HTTPException(
            status_code=503,
            detail="ANTHROPIC_API_KEY is not configured and no cached score exists for this lead.",
        )
    try:
        client = Anthropic(api_key=settings.anthropic_api_key)
        response = client.messages.create(
            model=settings.anthropic_model,
            max_tokens=1_200,
            messages=[{"role": "user", "content": _prompt(lead)}],
        )
    except APIStatusError as error:
        message = error.response.text
        if "credit balance is too low" in message.lower():
            if settings.allow_fallback_scoring:
                return _fallback_score(lead)
            raise HTTPException(
                status_code=402,
                detail=(
                    "Anthropic credit balance is too low. Add credits in Anthropic "
                    "Plans & Billing, or set ALLOW_FALLBACK_SCORING=true for local testing."
                ),
            ) from error
        raise HTTPException(
            status_code=error.status_code,
            detail=f"Anthropic API error: {message}",
        ) from error

    text = next((block.text for block in response.content if block.type == "text"), "{}")
    clean = text.replace("```json", "").replace("```", "").strip()
    return json.loads(clean)


def _to_frontend_shape(lead: dict[str, Any], score: AIScore, cached: bool) -> ScoredLead:
    company = lead.get("company") or lead.get("name") or "lead"
    identity = lead.get("website") or lead.get("email") or company
    dimensions = score.dimensions

    frontend_lead = {
        **lead,
        "company": company,
        "contact": lead.get("contact") or lead.get("owner") or lead.get("founder") or "Unknown contact",
        "title": lead.get("title") or "Unknown role",
        "industry": lead.get("industry") or lead.get("sector") or "Unknown",
        "location": lead.get("location") or lead.get("city") or lead.get("country") or "Unknown",
        "website": lead.get("website") or lead.get("domain") or "",
        "email": lead.get("email") or "",
        "employees": normalize_number(lead.get("employees") or lead.get("employee_count")),
        "revenue": normalize_number(lead.get("revenue") or lead.get("annual_revenue")),
        "notes": lead.get("notes") or lead.get("description") or "",
    }

    return ScoredLead(
        id=hashlib.sha1(str(identity).lower().encode("utf-8")).hexdigest(),
        score=score.overall_score,
        fit=round((dimensions.revenue_fit + dimensions.industry_fit + dimensions.size_fit) / 3),
        intent=round((dimensions.owner_reachability + dimensions.growth_signals) / 2),
        estimatedValue=max(5_000, round(frontend_lead["revenue"] * 0.35)) if frontend_lead["revenue"] else 0,
        reasons=score.fit_tags or [score.acquisition_thesis],
        nextAction=score.recommended_action,
        aiScore=score,
        cached=cached,
        lead=frontend_lead,
    )


async def score_lead(lead: LeadIn | dict[str, Any]) -> ScoredLead:
    lead_dict = lead if isinstance(lead, dict) else lead.model_dump(exclude_none=True)
    cache_key = lead_cache_key(lead_dict)

    cached_score = await get_cache(cache_key)
    if cached_score:
        score = AIScore.model_validate(cached_score)
        await save_scored_lead(lead_dict, score.model_dump(), cache_key, cache_hit=True)
        return _to_frontend_shape(lead_dict, score, cached=True)

    raw_score = _score_with_claude(lead_dict)
    score = AIScore.model_validate(raw_score)
    score_dict = score.model_dump()

    await set_cache(cache_key, score_dict)
    await save_scored_lead(lead_dict, score_dict, cache_key, cache_hit=False)

    return _to_frontend_shape(lead_dict, score, cached=False)
